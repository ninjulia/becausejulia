    <h1>Keeping Emails Compliant with CAN-SPAM, GDPR, and CCPA Regulations</h1>
    <p>At first glance, the current state of email regulations looks like a worrisome alphabet soup of legal hurdles and regulations. Nearly twenty countries and the EU have enacted email laws. The General Data Protection Regulation (GDPR) enacted in 2018 remains the most sweeping legislation to date. With it, EU citizens were granted new privacy rights and businesses regardless of location would be held accountable. Many feared this would be the end of email as we knew it.</p>
    <p>In actuality, complying with the GDPR laws have helped email marketers improve their ROI. As reported by Litmus, a majority of companies have seen increased email engagement rates, reduced opt-out rates, and lower spam complaints after implementing GDPR requirements. Looking ahead, ensuring compliance now is a smart move, as other countries and the state of California have enacted or are working to implement similar regulations to protect their citizens as well.</p>
    <h2>Lowest Hanging Fruit: CAN-SPAM</h2>
    <p>Enacted by the US, the Controlling the Assault of Non-Solicited Pornography And Marketing Act of 2003 was among the first email regulations enacted by law. It focused mainly on controlling spammers and set a low bar for compliance. Legally, businesses need only to adhere to the following guidelines:</p>
    <ul>
    <li>No deceptive "from" email addresses</li>
    <li>Don't use misleading subject lines</li>
    <li>Be clear that the email is an advertisement</li>
    <li>Include a physical address</li>
    <li>Allow recipients to opt-out</li>
    <li>Process opt-out requests within 10 business days</li>
    <li>Your company and any 3rd party senders will be held responsible for compliance</li>
    </ul>
    <p>Fines for non-compliance could be up to $43,280 per email. Luckily, these requirements have been "baked-in" to the initial set up phase of most Email Service Providers. The company address and unsubscribe links are automatically included at the bottom of every email sent.</p>
    <h2>Staying Out of the Spam Folder</h2>
    <p>Unfortunately, simply complying with the regulations does not mean your emails will even reach your recipients' inboxes. Notice that CAN-SPAM says nothing about obtaining permission before sending a user an email. According to Campaign Monitor, sending emails to recipients who have not provided express permission results in a 10x increase in spam complaints. Once your email address/domain is known for sending spam, it's nearly impossible to get off the blocklist.</p>
    <p>Having permission before you send makes good business sense too. In addition to higher click-through rates, Campaign Monitor reports "permission-based email lists have a 40x higher ROI than purchased or scraped email lists." Here are some tips for staying on the good side of Inbox Service Providers (Gmail, Outlook) and your email recipients:</p>
    <ul>
    <li>Only send to those who have elected to hear from you</li>
    <li>Confirm email signups via a double opt-in method</li>
    <li>Set up a preference center so users can choose how often they receive messaging and what type (ie: newsletter, promos) instead of unsubscribing completely</li>
    <li>A smaller list of engaged subscribers is worth more than a larger list with users who never open your message
    <ul>
    <li>Try sending a 're-engagement' campaign after 6 months of inactivity</li>
    <li>Auto-Unsubscribe users who aren't engaging after that</li>
    </ul>
    </li>
    </ul>
    <h2>GDPR: Permission Required</h2>
    <p>The General Data Protection Regulation puts a new focus on data privacy for users. It aims to limit the data collected by companies and gives users transparency into how their information will be used. GDPR is retroactive, applying to any information a company already has on file for users. This prompted many companies to start "re-engagement campaigns" to collect user consent before the law went into effect. Under the law, businesses must:</p>
    <ul>
    <li>Only email contacts who have opted-in to receive emails</li>
    <li>Purchased lists and other 3rd party data are no longer ok</li>
    <li>Separate marketing opt-in from any terms &amp; conditions or privacy statement acknowledgment checkboxes
    <ul>
    <li>These opt-ins CANNOT be selected by default</li>
    </ul>
    </li>
    <li>Explicitly state how a contact's information will be used</li>
    <li>Allow users to easily opt-out at any time</li>
    <li>Businesses must keep records of consent</li>
    </ul>
    <p>Penalties for non-compliance are stricter too, with fines of up to â‚¬20 Million or 4% of a brand's total global annual turnover (whichever is higher).</p>
    <h2>CCPA: Regulations for the US</h2>
    <p>Most recently, the State of California enacted their email regulation, granting similar protections to users as those in the EU. While the law is still in flux, businesses that made adjustments for GDPR should already be in compliance. The CCPA grants users the right to:</p>
    <ul>
    <li>Know what information is being collected about them</li>
    <li>Know if their info is being sold or disclosed and to whom</li>
    <li>Opt-out of the sale of their info</li>
    <li>Access the info stored on them</li>
    <li>Right to equal price, even if they exercise their privacy rights</li>
    </ul>
    <p>These regulations apply to companies with annual gross revenue of at least $25 million, data brokers, and/or businesses who focus on selling user data. Penalties for non-compliance include the possibility of a civil-action lawsuit, and/or direct charges from the state, with a $7,500 fine for each violation after 30 days.</p>
    <h2>Tips for CCPA Compliance</h2>
    <p>Again, businesses that comply with GDPR should not need to make any major changes to comply with the new CCPA laws. Having a good overall data management policy will help every email you send reach users who are interested in hearing from your business. Here are some policies you should keep in place:</p>
    <ul>
    <li>Do not send to 3rd party &amp; purchased lists</li>
    <li>Only collect the data you need at the time
    <ul>
    <li>Shorter forms lower abandon rates as well</li>
    </ul>
    </li>
    <li>Don't collect data you might never use
    <ul>
    <li>Limits your data liability</li>
    <li>Collect additional data via a campaign if needed</li>
    </ul>
    </li>
    <li>Allow users to manage their data and opt-out at any time</li>
    </ul>
